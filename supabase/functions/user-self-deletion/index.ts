import { createClient } from 'https://esm.sh/@supabase/supabase-js@2.14.0';

console.log(`Function "user-self-deletion" up and running!`);

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

Deno.serve(async (req) => {
  // This is needed if you're planning to invoke your function from a browser.
  if (req.method === 'OPTIONS') {
    return new Response('ok', { headers: corsHeaders });
  }

  try {
    // Create a Supabase client with the Auth context of the logged in user.
    const supabaseClient = createClient(
      // Supabase API URL - env var exported by default.
      Deno.env.get('SUPABASE_URL') ?? '',
      // Supabase API ANON KEY - env var exported by default.
      Deno.env.get('SUPABASE_ANON_KEY') ?? '',
      // Create client with Auth context of the user that called the function.
      // This way your row-level-security (RLS) policies are applied.
      { global: { headers: { Authorization: req.headers.get('Authorization') } } }
    );
    // Now we can get the session or user object
    console.log('Obtaining user session');
    const {
      data: { user },
    } = await supabaseClient.auth.getUser();
    // And we can run queries in the context of our authenticated user
    if (!user) throw userError as Error;
    const userId = user.id;
    console.log('userId: ' + userId);

    const supabaseAdmin = createClient(
      Deno.env.get('SUPABASE_URL') ?? '',
      Deno.env.get('SUPABASE_SERVICE_ROLE_KEY') ?? ''
    );

    const { data: deletionData, error: deletionError } =
      await supabaseAdmin.auth.admin.deleteUser(userId);
    if (deletionError) throw deletionError;
    console.log('User userId: ' + userId + ' deleted');
    return new Response(JSON.stringify(deletionData, null, 2), {
      headers: { ...corsHeaders, 'Content-Type': 'application/json' },
      status: 200,
    });
  } catch (error) {
    return new Response(JSON.stringify({ error: error.message }), {
      headers: { ...corsHeaders, 'Content-Type': 'application/json' },
      status: 400,
    });
  }
});

